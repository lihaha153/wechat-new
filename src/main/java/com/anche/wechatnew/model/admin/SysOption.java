package com.anche.wechatnew.model.admin;

public class SysOption {
    private String id;
    private String option_kind;
    private String option_name;
    private String option_des;
    private String option_code;
    private String is_stop;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getOption_kind() {
        return option_kind;
    }

    public void setOption_kind(String option_kind) {
        this.option_kind = option_kind;
    }

    public String getOption_name() {
        return option_name;
    }

    public void setOption_name(String option_name) {
        this.option_name = option_name;
    }

    public String getOption_des() {
        return option_des;
    }

    public void setOption_des(String option_des) {
        this.option_des = option_des;
    }

    public String getOption_code() {
        return option_code;
    }

    public void setOption_code(String option_code) {
        this.option_code = option_code;
    }

    public String getIs_stop() {
        return is_stop;
    }

    public void setIs_stop(String is_stop) {
        this.is_stop = is_stop;
    }
}
